#include"libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	i;

	i = 0;
	while (n - 1 > 0 && s1[i] - s2[i] == 0)
	{
		i++;
		n--;
	}
	return (s1[i] - s2[i]);
}
